import { useEffect, useState } from 'react';

import { ThreadDtoStatusEnum } from '../../../autogenerated/api';

export function useLiveThreadDuration(args: {
  runningStartedAt: string | null | undefined;
  totalRunningMs: number | undefined;
  status: ThreadDtoStatusEnum | string | undefined;
}): number {
  const { runningStartedAt, totalRunningMs, status } = args;

  const [nowMs, setNowMs] = useState<number>(() => Date.now());

  const isRunning = status === ThreadDtoStatusEnum.Running;

  const startedAtMs =
    isRunning && runningStartedAt != null
      ? new Date(runningStartedAt).getTime()
      : NaN;

  const validStartedAt = !Number.isNaN(startedAtMs);

  useEffect(() => {
    if (!isRunning || !validStartedAt) {
      return;
    }

    setNowMs(Date.now()); // immediate refresh on transition into Running
    const id = setInterval(() => {
      setNowMs(Date.now());
    }, 1000);

    return () => {
      clearInterval(id);
    };
  }, [isRunning, validStartedAt, startedAtMs]);

  if (isRunning && validStartedAt) {
    return (totalRunningMs ?? 0) + (nowMs - startedAtMs);
  }

  return totalRunningMs ?? 0;
}
